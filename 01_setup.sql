-- 1. Create Warehouse
CREATE WAREHOUSE IF NOT EXISTS SHOPSTREAM_WH
    WAREHOUSE_SIZE = 'XSMALL'
    AUTO_SUSPEND = 60
    AUTO_RESUME = TRUE;

-- 2. Create Database and Schemas
CREATE DATABASE IF NOT EXISTS SHOPSTREAM;
CREATE SCHEMA IF NOT EXISTS SHOPSTREAM.RAW;
CREATE SCHEMA IF NOT EXISTS SHOPSTREAM.ANALYTICS;

USE WAREHOUSE SHOPSTREAM_WH;
USE DATABASE SHOPSTREAM;
USE SCHEMA RAW;

-- 3. Create Tables
CREATE OR REPLACE TABLE CUSTOMERS (
    customer_id INT,
    first_name VARCHAR(50),
    last_name VARCHAR(50),
    email VARCHAR(100),
    region VARCHAR(30),
    signup_date DATE
);

CREATE OR REPLACE TABLE PRODUCTS (
    product_id INT,
    product_name VARCHAR(100),
    category VARCHAR(50),
    unit_price DECIMAL(10, 2)
);

CREATE OR REPLACE TABLE ORDERS (
    order_id INT,
    customer_id INT,
    product_id INT,
    quantity INT,
    order_date DATE
);

